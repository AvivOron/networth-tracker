export function t(key: string, lang: Language | string): string {
  const lang_val = (lang === 'he' || lang === 'en') ? lang : 'en'
  return translations[key]?.[lang_val] ?? translations[key]?.['en'] ?? key
}

export function tn(key: string, n: number, lang: Language | string): string {
  const template = t(key, lang)
  return template.replace('{n}', String(n))
}
